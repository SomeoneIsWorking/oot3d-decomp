/*
 * OoT3D primitive typedefs. Match N64 OoT decomp's `ultratypes.h` shape
 * so per-file hand-decomps can be diffed against their N64 twins.
 */
#ifndef OOT3D_Z_TYPES_H
#define OOT3D_Z_TYPES_H

#include <stdint.h>

typedef int8_t   s8;
typedef int16_t  s16;
typedef int32_t  s32;
typedef int64_t  s64;
typedef uint8_t  u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;
typedef float    f32;
typedef double   f64;

#endif /* OOT3D_Z_TYPES_H */
